package com.lourenco;

public class Calculator {

    public double half(double num1) {
        return (num1 / 2);
    }

    public double quarter(double num1) {
        return (num1 / 4);
    }

    public double theDouble(double num1) {
        return (num1 * 2);
    }

    public double triple(double num1) {
        return (num1 * 3);
    }

    public double sum(double num1, double num2) {
        return (num1 + num2);
    }

    public double subtraction(double num1, double num2) {
        return (num1 - num2);
    }

    public double multiply(double num1, double num2) {
        return (num1 * num2);
    }

    public double division(double num1, double num2) {
        return (num1 / num2);
    }
}

